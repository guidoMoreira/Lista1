package exe1;


public class Exercicio2 {
    public float x;
    public float y;
    public Exercicio2(float x, float y){
        this.x = x;
        this.y = y;
    }
    public float Som(){
        return x + y;
    }
    public float Sub(){
        return x - y;
    }
    public float Mult(){
        return x * y;
    }
    public float Div(){
        return x / y;
    }

}
